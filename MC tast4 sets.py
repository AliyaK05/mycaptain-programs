

set1 = {10,20,30,40,50}
set2 = {5,10,15,20,25}

# set union
print("Union of both the sets is",set1 | set2)

# set intersection
print("Intersection of both the sets is",set1 & set2)

# set difference
print("Difference of both the sets is",set1 - set2 ,"and", set2-set1)

# set symmetric difference
print("Symmetric difference of both the sets is",set1 ^ set2)
    
